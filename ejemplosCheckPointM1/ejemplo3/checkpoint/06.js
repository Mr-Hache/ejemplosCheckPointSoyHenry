function sortPrimeHouses(array) {
  // Tu c√≥digo aqu√≠:
  let countPrimos = 0;
  for (let i = 0; i < array.length; i++) {
    let esPrimo = 0;
    for (let j = 1; j < array[i]; j++) {
      if (array[i] % j === 0) {
        esPrimo++;
      }
    }
    if (esPrimo > 2) countPrimos++;
  }
  if (countPrimos > 0) return false;
  for (let i = 1; i < array.length; i++) {
    let aux = array[i];
    let j = i - 1;
    while (j >= 0 && array[j] > aux) {
      array[j + 1] = array[j];
      j--;
    }
    array[j + 1] = aux;
  }
  return array;
}

